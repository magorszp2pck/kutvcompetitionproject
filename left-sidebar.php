            <h2>XIV. Középiskolások Üzleti Tanácsadó Versenye</h2>
<p>

A Babeş-Bolyai Tudományegyetem Közgazdaság- és Gazdálkodástudományi Magyar Intézetének szervezésében, 2022. április 8-9. között, tizennegyedik alkalommal kerül megrendezésre a Kolozsvári Esettanulmány Napok rendezvénysorozat. Az Esettanulmány Napok keretén belül kiemelt helyet kap a már komoly hagyományokkal rendelkező Középiskolások Üzleti Tanácsadó Versenyének döntő fordulója.</p>
<h3>Miért jelentkezz?</h3>
<p>

A Középiskolások Üzleti Tanácsadó Versenye magyar anyanyelvű középiskolás diákoknak szervezett gazdasági verseny. Célunk a közgazdász szakma rejtélyeibe betekintést nyújtani a pályaválasztás előtt álló fiatalok számára. Ennek érdekében egyszerű, gyakorlatias és szórakoztató tanulási módszerek segítségével építettük fel a Középiskolások Üzleti Tanácsadó Versenyét. A verseny alatt a diákok megismerkedhetnek a piackutatás területével, majd az itt szerzett ismereteket egy esettanulmány megoldása közben hasznosíthatják.
              </p>
              <p>
Az előző évekhez hasonlóan a hagyományosan Kolozsváron megszervezett verseny képezi a döntő szakaszt, amelyet két online forduló előz meg. A verseny első két fordulóját egy piackutatási probléma képezi, a döntő forduló pedig egy összetettebb vállalatvezetői probléma megoldását feltételezi. A fordulókat úgy építettük fel, hogy felkészítsék a diákokat a döntőben sorra kerülő eset megoldására.              </p>

<h3>Hogyan épül fel a verseny?</h3>
<p>
A döntőben a két online forduló alapján legjobban teljesítő, egyenként 3 fős középiskolai csapatok vesznek részt. A döntőben dobogós helyezést elérő csapatok tagjai, a díjazás mellett, a Közgazdaság- és Gazdálkodástudományi Karra történő felvételi esetén plusz pontokat kapnak az érettségi átlagukhoz.
A Középiskolások Üzleti Tanácsadó Versenyének döntőjével párhuzamosan zajlik a Kolozsvári Egyetemista Esettanulmány Verseny, melynek döntőjét a középiskolás diákok nézőként követhetik..
              </p>
              <h3>Kik jelentkezhetnek?</h3>

<p>
A versenyre 3 fős középiskolás diákokból álló csapatok jelentkezhetnek az oldalon elérhető regisztrációs űrlap kitöltésével.
              </p>
 
<h4>1. Piackutatás: adatgyűjtés – online – szelekció</h4>
<p>

A jelentkező csapatoknak a feladata egy piackutatás elvégzése megadott mintavételi és módszertani kritériumok figyelembevételével. Azok a csapatok, amelyek sikeresen teljesítik az első fordulót, továbbjutnak a második fordulóba.
              </p>
              <h4>2. Piackutatás: adatfeldolgozás - online – szelekció</h4>

<p>
A második fordulóban a csapatok által beküldött adatok és más, már meglévő, piaci információk alapján a csapatoknak egy elemzést kell készíteniük egy adott témában. A második forduló részletes leírását és az elemzés szempontjait a verseny ütemtervében megjelölt időpontban kiküldjük a csapatoknak.
              </p>

<h4>3. Esettanulmány döntő - Kolozsvár, Babeș Bolyai Tudományegyetem, Közgazdaság- és Gazdálkodástudományi Kar</h4>
<p>
A második forduló során továbbjutott csapatok a döntőben mérik össze tudásukat és készségeiket egy valós vállalati esettanulmány megoldása és bemutatása során. Az egy hét alatt otthoni környezetben elkészített megoldást a döntősök Kolozsváron, a Babeş-Bolyai Tudományegyetem Közgazdaság- és Gazdálkodástudományi Karának székhelyén zajló döntő második napján gyakorló (nem oktató) szakemberekből álló zsűri előtt mutatják be.</p>
